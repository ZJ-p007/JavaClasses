/*
  标识符：
    组成：英文大小写，数字
    命名规则：
      遵循驼峰
      类和接口：所有单词的首字母大写，其他小写
      方法和常量：第二单词开始，所有单词首字母大写，其他全部小写
      常量：全部大写，单词之间用_隔开
      包名：全部小写
 */
public class IdentifierDemo {
    public static void main(String[] args) {
        //定义变量，记录张三得年龄
        int zhangSanAge = 23;
        System.out.println(zhangSanAge);

        int b4 = 30;
        System.out.println(b4);


    }
}
